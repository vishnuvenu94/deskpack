import fs from "node:fs";
import path from "node:path";
import type { DeskpackConfig } from "../types.js";
import { log } from "../utils/logger.js";

/**
 * Copy TanStack Start Node/Nitro output into the packaged server resources.
 */
export function copyTanstackStartRuntime(
  rootDir: string,
  config: DeskpackConfig,
  serverDir: string,
): void {
  const runtime = config.frontend.tanstackStart?.runtime;
  if (!runtime || runtime.mode !== "node") {
    throw new Error("TanStack Start runtime metadata is missing from deskpack.config.json.");
  }

  const outputSource = path.resolve(rootDir, runtime.outputDir);
  const serverFile = path.resolve(rootDir, runtime.serverFile);
  if (!fs.existsSync(serverFile)) {
    throw new Error(
      `TanStack Start server output not found at ${serverFile}. ` +
        "Ensure the build emits Nitro Node output at .output/server/index.mjs.",
    );
  }

  const destination = path.join(serverDir, "tanstack");
  copyDirSync(outputSource, path.join(destination, ".output"));
  writeLauncher(path.join(destination, "start.cjs"));

  log.success(`Copied TanStack Start runtime -> ${path.relative(rootDir, destination)}`);
}

function writeLauncher(launcherPath: string): void {
  fs.mkdirSync(path.dirname(launcherPath), { recursive: true });
  fs.writeFileSync(
    launcherPath,
    [
      "// Generated by deskpack - do not edit manually",
      "const path = require('node:path');",
      "const { pathToFileURL } = require('node:url');",
      "const entry = path.join(__dirname, '.output', 'server', 'index.mjs');",
      "import(pathToFileURL(entry).href).catch((error) => {",
      "  console.error(error);",
      "  process.exit(1);",
      "});",
      "",
    ].join("\n"),
  );
}

function copyDirSync(src: string, dest: string): void {
  fs.mkdirSync(dest, { recursive: true });

  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);

    if (entry.isDirectory()) {
      copyDirSync(srcPath, destPath);
    } else {
      fs.copyFileSync(srcPath, destPath);
    }
  }
}
