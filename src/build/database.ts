export function detectHardcodedSqlitePaths(
  rootDir: string,
  config?: DeskpackConfig,
): string[] {
  const detected = new Set<string>();

  walkSourceFiles(rootDir, (file) => {
    collectSqlitePathHints(rootDir, fs.readFileSync(file, "utf-8"), detected);
  });

  if (config?.database?.templatePath) {
    addSqlitePathVariants(rootDir, config.database.templatePath, detected);
  }

  return [...detected].sort();
}

