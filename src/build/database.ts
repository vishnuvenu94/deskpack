import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";
import type { DeskpackConfig } from "../types.js";
import { log } from "../utils/logger.js";

const SQLITE_FILE_PATTERN = /(?:^|[/\\])[^/\\]+\.(?:db|sqlite|sqlite3)$/i;
const SQLITE_LITERAL_PATTERN = /["'`]([^"'`]*\.(?:db|sqlite|sqlite3))["'`]/gi;

export function copyDatabaseAssets(
  rootDir: string,
  config: DeskpackConfig,
  serverDir: string,
): void {
  if (!config.database) return;

  const destination = path.join(serverDir, "database");
  fs.mkdirSync(destination, { recursive: true });

  const templateDbPath = path.join(destination, "template.db");
  if (config.database.templatePath) {
    const source = path.resolve(rootDir, config.database.templatePath);
    if (!fs.existsSync(source)) {
      throw new Error(`Configured SQLite template database not found at ${source}.`);
    }
    fs.copyFileSync(source, templateDbPath);
  }

  const migrations = config.database.migrations;
  if (migrations?.path) {
    const source = path.resolve(rootDir, migrations.path);
    if (fs.existsSync(source)) {
      copyDirSync(source, path.join(destination, "migrations"));
      if (!fs.existsSync(templateDbPath)) {
        createTemplateDatabaseFromSqlMigrations(source, templateDbPath);
      }
    }
  }

  const hardcodedPaths = detectHardcodedSqlitePaths(rootDir, config);
  if (hardcodedPaths.length > 0) {
    fs.writeFileSync(
      path.join(destination, "sqlite-preload.cjs"),
      createManagedSqlitePreload(hardcodedPaths),
    );
  }

  log.success(`Prepared SQLite database assets -> ${path.relative(rootDir, destination)}`);
}

export function createManagedSqlitePreload(paths: string[]): string {
  return `// Generated by deskpack - redirects hardcoded SQLite paths to Electron userData.
const Module = require("module");
const path = require("path");

const runtimePath = process.env.DESKPACK_DB_PATH;
const hardcodedPaths = new Set(${JSON.stringify(paths)});
const originalLoad = Module._load;

function normalize(value) {
  return String(value).replace(/\\\\/g, "/").replace(/^\\.\\//, "");
}

function pathMatchesKnownSqlitePath(filename) {
  const normalized = normalize(filename);
  if (hardcodedPaths.has(filename) || hardcodedPaths.has(normalized)) return true;
  if (hardcodedPaths.has(normalize(path.resolve(process.cwd(), filename)))) return true;

  for (const hardcodedPath of hardcodedPaths) {
    const normalizedHardcodedPath = normalize(hardcodedPath);
    if (normalizedHardcodedPath.startsWith("/") || !normalizedHardcodedPath.includes("/")) continue;
    if (normalized.endsWith("/" + normalizedHardcodedPath)) return true;
  }

  return false;
}

function shouldRewrite(filename) {
  if (!runtimePath || typeof filename !== "string") return false;
  if (filename === ":memory:" || filename.startsWith("file:")) return false;
  return pathMatchesKnownSqlitePath(filename);
}

function patchDatabase(Database) {
  if (typeof Database !== "function" || Database.__deskpackPatched) return Database;

  function PatchedDatabase(filename, ...args) {
    return new Database(shouldRewrite(filename) ? runtimePath : filename, ...args);
  }

  Object.setPrototypeOf(PatchedDatabase, Database);
  PatchedDatabase.prototype = Database.prototype;
  Object.defineProperty(PatchedDatabase, "__deskpackPatched", { value: true });
  return PatchedDatabase;
}

function patchSqlite3(sqlite3) {
  if (!sqlite3 || typeof sqlite3.Database !== "function" || sqlite3.__deskpackPatched) return sqlite3;

  const OriginalDatabase = sqlite3.Database;
  function PatchedSqlite3Database(filename, ...args) {
    return new OriginalDatabase(shouldRewrite(filename) ? runtimePath : filename, ...args);
  }

  Object.setPrototypeOf(PatchedSqlite3Database, OriginalDatabase);
  PatchedSqlite3Database.prototype = OriginalDatabase.prototype;
  sqlite3.Database = PatchedSqlite3Database;
  Object.defineProperty(sqlite3, "__deskpackPatched", { value: true });
  return sqlite3;
}

function isPackageRequest(request, packageName) {
  if (request === packageName || request.startsWith(packageName + "/")) return true;

  const normalized = normalize(request);
  const nodeModulesSegment = "/node_modules/" + packageName + "/";
  return normalized.includes(nodeModulesSegment);
}

Module._load = function deskpackLoad(request, parent, isMain) {
  const loaded = originalLoad.apply(this, arguments);
  if (isPackageRequest(request, "better-sqlite3") || /^better-sqlite3-/.test(request)) {
    return patchDatabase(loaded);
  }
  if (isPackageRequest(request, "sqlite3")) {
    return patchSqlite3(loaded);
  }
  return loaded;
};
`;
}

export function createManagedBetterSqlite3Shim(paths: string[]): string {
  return `// Generated by deskpack - redirects hardcoded better-sqlite3 paths to Electron userData.
${createManagedSqlitePathMatcherSource(paths)}
const OriginalDatabase = require("better-sqlite3");

function Database(filename, ...args) {
  return new OriginalDatabase(shouldRewrite(filename) ? runtimePath : filename, ...args);
}

Object.setPrototypeOf(Database, OriginalDatabase);
Database.prototype = OriginalDatabase.prototype;

export { Database };
export default Database;
`;
}

