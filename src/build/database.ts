function collectSqlitePathHints(
  rootDir: string,
  content: string,
  detected: Set<string>,
): void {
  const dataDirLikely = /["'`]data["'`]/.test(content);

  for (const match of content.matchAll(SQLITE_LITERAL_PATTERN)) {
    const value = normalizeSqlitePath(match[1]);
    if (dataDirLikely && value && !value.includes("/")) continue;
    addSqlitePathVariants(rootDir, match[1], detected);
  }

  if (!dataDirLikely) return;

  for (const match of content.matchAll(/["'`]([^"'`]+\.(?:db|sqlite|sqlite3))["'`]/gi)) {
    const value = normalizeSqlitePath(match[1]);
    if (!value || value.includes("/")) continue;
    addSqlitePathVariants(rootDir, path.posix.join("data", value), detected);
  }
}

function addSqlitePathVariants(
  rootDir: string,
  rawPath: string,
  detected: Set<string>,
): void {
  const normalized = normalizeSqlitePath(rawPath);
  if (!normalized || !SQLITE_FILE_PATTERN.test(normalized)) return;

  const variants = new Set<string>([normalized]);
  if (!path.isAbsolute(normalized)) {
    variants.add(`./${normalized.replace(/^\.\//, "")}`);
    variants.add(path.resolve(rootDir, normalized));
  }

  const basename = path.posix.basename(normalized);
  if (!normalized.includes("/") && basename === normalized) {
    variants.add(path.posix.join("data", basename));
    variants.add(`./${path.posix.join("data", basename)}`);
    variants.add(path.resolve(rootDir, "data", basename));
  }

  for (const variant of variants) {
    detected.add(normalizeSqlitePath(variant));
  }
}

function normalizeSqlitePath(value: string): string {
  return value.trim().replace(/\\/g, "/").replace(/^file:/, "").replace(/^\.\//, "");
}

function createTemplateDatabaseFromSqlMigrations(
  migrationsDir: string,
  templateDbPath: string,
): void {
  const sqlFiles = collectSqlMigrationFiles(migrationsDir);
  if (sqlFiles.length === 0) return;

  fs.mkdirSync(path.dirname(templateDbPath), { recursive: true });
  const result = spawnSync("sqlite3", [templateDbPath], {
    input: sqlFiles.map((file) => fs.readFileSync(file, "utf-8")).join("\n"),
    encoding: "utf-8",
  });

  if (result.error) {
    log.warn(`Could not create SQLite template from migrations: ${result.error.message}`);
    return;
  }

  if (result.status !== 0) {
    log.warn(`Could not create SQLite template from migrations: ${result.stderr.trim()}`);
    try {
      fs.rmSync(templateDbPath, { force: true });
    } catch {
      // Best effort cleanup only.
    }
  }
}

function collectSqlMigrationFiles(migrationsDir: string): string[] {
  const files: string[] = [];

  const visit = (dir: string): void => {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        if (entry.name !== "meta") visit(fullPath);
        continue;
      }
      if (entry.isFile() && entry.name.endsWith(".sql")) {
        files.push(fullPath);
      }
    }
  };

  visit(migrationsDir);
  return files.sort();
}

function copyDirSync(src: string, dest: string): void {
  fs.mkdirSync(dest, { recursive: true });

  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);

    if (entry.isDirectory()) {
      copyDirSync(srcPath, destPath);
    } else {
      fs.copyFileSync(srcPath, destPath);
    }
  }
}

function walkSourceFiles(rootDir: string, onFile: (file: string) => void): void {
  const stack = [rootDir];
  const allowedExtensions = new Set([
    ".js",
    ".jsx",
    ".mjs",
    ".cjs",
    ".ts",
    ".tsx",
    ".mts",
    ".cts",
  ]);
  const skippedDirs = new Set([
    "node_modules",
    ".git",
    ".deskpack",
    ".next",
    "dist",
    "build",
    "coverage",
  ]);

  while (stack.length > 0) {
    const dir = stack.pop() as string;
    let entries: fs.Dirent[];
    try {
      entries = fs.readdirSync(dir, { withFileTypes: true });
    } catch {
      continue;
    }

    for (const entry of entries) {
      const full = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        if (!skippedDirs.has(entry.name)) stack.push(full);
        continue;
      }
      if (entry.isFile() && allowedExtensions.has(path.extname(entry.name))) {
        onFile(full);
      }
    }
  }
}
